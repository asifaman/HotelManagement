    private void searchkeyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchkeyKeyReleased
        // TODO add your handling code here:
         String searchCol = "";
         int count = 0;
          if (searchcbox.getSelectedIndex() == 0) {
            searchCol = "GUEST_NAME";
        } else if (searchcbox.getSelectedIndex() == 1) {
            searchCol = "GUEST_PHONENO";
        } else if (searchcbox.getSelectedIndex() == 2) {
            searchCol = "GUEST_ADDRESS";
        } else if( searchcbox.getSelectedIndex() == 3) {
            searchCol = "ROOM_NO";
        }else if (searchcbox.getSelectedIndex() == 4) {
            searchCol = "ROOM_TYPE";
        }else if (searchcbox.getSelectedIndex() == 5) {
            searchCol = "UPZILLA";
        }else if (searchcbox.getSelectedIndex() == 6) {
            searchCol = "ZILLA";
        }else if (searchcbox.getSelectedIndex() == 7) {
            searchCol = "COMPANY";
        }else if (searchcbox.getSelectedIndex() == 8) {
            searchCol = "JOB_TITLE";
        }else if (searchcbox.getSelectedIndex() == 9) {
            searchCol = "FROM_DATE";
        }else if (searchcbox.getSelectedIndex() == 10) {
            searchCol = "TO_DATE";
        }

        String sql = "SELECT count(GUEST_NAME) FROM manager.guestlist WHERE " + searchCol + " LIKE '" + searchkey.getText() + "%'";
        count = Utility2.countQuery(searchkey, sql);

        data = new Object[count][4];

        sql = "SELECT * FROM manager.guestlist WHERE " + searchCol + " LIKE '" + searchkey.getText() + "%'";
        data = Utility2.getMultiRowQueryObjects(sql, searchcbox, count);


        Object[] header = {"GUEST NAME", "PHONE NO", "GUEST ADDRESS", "ROOM NO", "ROOM TYPE", "UPZILLA", "ZILLA", "COMPANY", "JOB TITLE", "FROM DATE", "TO DATE"};
        DefaultTableModel dtm = new DefaultTableModel(data, header);
        table.setModel(dtm);
  
        
    }//GEN-LAST:event_searchkeyKeyReleased

